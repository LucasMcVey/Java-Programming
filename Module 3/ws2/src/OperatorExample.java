public class OperatorExample {
    public static void main(String[] args) {
        int x = 3;
        x = x + 1;
        System.out.println(x);
        x += 1;
        System.out.println(x);
        x++;
        System.out.println(x);

        x--;
        System.out.println(x);
        x -= 5;
        System.out.println(x);
    }
}
